# ============================================================
# 1)  RBF SURROGATE  (thin-plate-spline kernel, direct h → R / V)
# ============================================================

def _tps_kernel(r: np.ndarray) -> np.ndarray:
    """Thin-plate-spline kernel  φ(r) = r² log(r),  with φ(0) = 0."""
    r = np.maximum(r, 1e-300)
    return r**2 * np.log(r)


def fit_rbf(
    X_train: np.ndarray,
    y_train: np.ndarray,
    lam: float = 1e-4,
) -> dict:
    """
    Fit an RBF surrogate:  ŷ(x) = Σ w_i φ(‖x-x_i‖) + p(x)
    where p(x) = c₀ + c₁·x₁ + … is a linear polynomial trend.

    Parameters
    ----------
    X_train : (n, d)  training inputs
    y_train : (n,)    training targets
    lam     : float   Tikhonov regularisation (prevents overfitting to noise)

    Returns
    -------
    model dict containing weights, polynomial coefficients, and
    normalisation statistics needed for prediction.
    """
    n, d = X_train.shape
    y_train = y_train.reshape(-1, 1)

    # Standardise inputs → better-conditioned distance matrix
    mu_x  = X_train.mean(0)
    sig_x = X_train.std(0);  sig_x[sig_x < 1e-12] = 1.0
    Z     = (X_train - mu_x) / sig_x                    # (n, d)

    # Standardise output
    mu_y  = float(y_train.mean())
    sig_y = float(y_train.std());  sig_y = max(sig_y, 1e-12)
    Ys    = (y_train - mu_y) / sig_y                     # (n, 1)

    # Pairwise distances between training centres
    diff  = Z[:, None, :] - Z[None, :, :]               # (n, n, d)
    r_mat = np.sqrt(np.sum(diff**2, axis=-1))            # (n, n)
    Phi   = _tps_kernel(r_mat)                           # (n, n)

    # Polynomial trend basis  P = [1, z₁, …, z_d]  shape (n, d+1)
    P = np.hstack([np.ones((n, 1)), Z])                  # (n, d+1)

    # Solve the block-symmetric RBF system:
    #   | Phi + λI   P  | | w |   | Ys |
    #   |  Pᵀ        0  | | c | = |  0 |
    n_poly = d + 1
    A = np.zeros((n + n_poly, n + n_poly))
    A[:n,  :n]  = Phi + lam * np.eye(n)
    A[:n,  n:]  = P
    A[n:,  :n]  = P.T
    # bottom-right block stays zero (unbiased polynomial conditions)

    rhs       = np.zeros((n + n_poly, 1))
    rhs[:n]   = Ys

    sol = np.linalg.solve(A, rhs)                        # (n + d+1, 1)
    w   = sol[:n].flatten()                              # RBF weights
    c   = sol[n:].flatten()                              # polynomial coeffs

    return dict(
        Z_train=Z, w=w, c=c,
        mu_x=mu_x, sig_x=sig_x,
        mu_y=mu_y, sig_y=sig_y,
        n=n, d=d,
    )


def predict_rbf_numpy(x: np.ndarray, model: dict) -> np.ndarray:
    """
    Evaluate the RBF surrogate at one or many points (pure numpy).

    Parameters
    ----------
    x     : (d,) or (m, d)
    model : dict returned by fit_rbf

    Returns
    -------
    y : (m,) predicted values  [or scalar if single input]
    """
    single = (x.ndim == 1)
    if single:
        x = x.reshape(1, -1)

    z    = (x - model["mu_x"]) / model["sig_x"]          # (m, d)
    diff = z[:, None, :] - model["Z_train"][None, :, :]  # (m, n, d)
    r    = np.sqrt(np.sum(diff**2, axis=-1))              # (m, n)
    phi  = _tps_kernel(r)                                 # (m, n)
    P    = np.hstack([np.ones((len(z), 1)), z])           # (m, d+1)

    ys   = phi @ model["w"] + P @ model["c"]             # (m,)
    y    = ys * model["sig_y"] + model["mu_y"]

    return float(y[0]) if single else y


def predict_rbf_csdl(h_var: csdl.Variable, model: dict) -> csdl.Variable:
    """
    Evaluate the RBF surrogate inside a CSDL computation graph.
    Enables exact automatic differentiation for the optimizer.

    φ(r) = r² log(r) = ½ · r² · log(r²)   (avoids sqrt, numerically stable)

    Parameters
    ----------
    h_var : csdl.Variable  shape (29,)
    model : dict returned by fit_rbf

    Returns
    -------
    scalar csdl.Variable  (predicted R or V)
    """
    Z_train = model["Z_train"]   # (n, d)  numpy constant
    w       = model["w"]         # (n,)    numpy constant
    c       = model["c"]         # (d+1,)  numpy constant
    mu_x    = model["mu_x"]
    sig_x   = model["sig_x"]
    mu_y    = float(model["mu_y"])
    sig_y   = float(model["sig_y"])
    n       = model["n"]

    # Standardise input
    z = (h_var - mu_x) / sig_x                           # (d,)

    # Compute φ(‖z - zᵢ‖) for each training centre i
    # φ(r) = ½ r² log(r²),  so we work with r² = ‖diff‖² to avoid sqrt
    phi_list = []
    for i in range(n):
        diff_i  = z - Z_train[i]                          # (d,)
        r2      = csdl.sum(diff_i * diff_i)               # scalar
        # Clamp to avoid log(0)
        r2_safe = csdl.maximum(r2, csdl.Variable(value=np.array([1e-300])))
        phi_i   = 0.5 * r2_safe * csdl.log(r2_safe)      # ½ r² log(r²)
        phi_list.append(csdl.reshape(phi_i, (1,)))

    phi_vec = csdl.concatenate(phi_list)                  # (n,)

    # Polynomial trend [1, z₁, …, z_d]
    ones  = csdl.Variable(value=np.ones(1))
    p_vec = csdl.concatenate([ones, z])                   # (d+1,)

    ys = csdl.sum(phi_vec * w) + csdl.sum(p_vec * c)     # scalar
    return ys * sig_y + mu_y


