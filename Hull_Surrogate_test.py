def train_test_split(
    H: np.ndarray,
    R: np.ndarray,
    V: np.ndarray,
    test_fraction: float = 0.5,
) -> tuple:
    """
    Split dataset chronologically (first half train, second half test).
    Chronological split is preferred over random here because the Halton
    sequence already provides uniform coverage — random shuffling would
    give the same spatial coverage to both halves anyway, but chronological
    is more transparent and reproducible.

    Returns
    -------
    H_train, R_train, V_train, H_test, R_test, V_test
    """
    n      = len(R)
    n_test = int(n * test_fraction)
    split  = n - n_test            # train: [0, split),  test: [split, n)

    return (H[:split], R[:split], V[:split],
            H[split:], R[split:], V[split:])


def validate_surrogate(
    H_test: np.ndarray,
    R_test: np.ndarray,
    V_test: np.ndarray,
    R_surr: dict,
    V_surr: dict,
) -> dict:
    """
    Compare surrogate predictions against truth-model values on the test set.

    This is exactly what your partner described:
      - surrogate was trained on H_train
      - we now predict R and V for H_test
      - compare to the truth-model values R_test, V_test that were
        computed by generate_dataset.py and saved in the CSV
      → quantifies how well the surrogate generalises to unseen designs

    Returns a dict of metrics.
    """
    R_pred = predict_rbf_numpy(H_test, R_surr)
    V_pred = predict_rbf_numpy(H_test, V_surr)

    def rmse(a, b): return float(np.sqrt(np.mean((a - b)**2)))
    def mae(a,  b): return float(np.mean(np.abs(a - b)))
    def r2(a,   b):
        ss_res = np.sum((a - b)**2)
        ss_tot = np.sum((a - a.mean())**2)
        return float(1.0 - ss_res / ss_tot) if ss_tot > 0 else 0.0

    metrics = dict(
        R_rmse = rmse(R_test, R_pred),
        R_mae  = mae( R_test, R_pred),
        R_r2   = r2(  R_test, R_pred),
        V_rmse = rmse(V_test, V_pred),
        V_mae  = mae( V_test, V_pred),
        V_r2   = r2(  V_test, V_pred),
        R_pred = R_pred,
        V_pred = V_pred,
    )
    return metrics


