# ============================================================
# 5)  SPLINE FITTING THROUGH OPTIMAL CONTROL POINTS
# ============================================================
#
# The optimizer outputs h_opt → a set of Bezier control points (P1..P5,
# D1..D4, B1..B4, FK, FW). These define the hull shape discretely.
#
# We now fit smooth parametric B-splines THROUGH those control points.
# This is the "line fitting" step: we go from a set of optimizer-chosen
# points to a continuous smooth curve that defines each hull feature.
#
# Why do this on top of Bezier curves?
#   The Bezier curves use fixed topology (4-5 control points per curve).
#   A B-spline fit gives us a smooth interpolating curve that passes
#   exactly through the optimizer's control points — this is the formal
#   definition of the optimal hull geometry as a continuous function,
#   independent of the Bezier parameterization.
#
# We fit three curves:
#   1. Keel profile   — spline through P1..P5  (side view, x-z plane)
#   2. Deck edge      — spline through D1..D4  (plan view, x-y plane)
#   3. Keel planform  — spline through B1..B4  (bottom view, x-y plane)
#
# We also fit a cubic spline to the cross-sectional half-beam and draft
# as functions of x — this gives a smooth, exportable hull offset table.
#

def fit_splines_to_control_points(h_opt: np.ndarray) -> dict:
    """
    Fit parametric B-splines through the optimal Bezier control points.

    Returns a dict of spline objects and evaluated curves for plotting
    and export.
    """
    cp  = build_control_points(h_opt, **CONST)
    cv  = hull_curves(cp, n=500)
    L   = CONST["L"]

    results = {}

    # ── 1. Keel profile spline (x-z plane) ───────────────────────────────────
    # Control points: P1, P2, P3, P4, P5
    keel_pts = np.array([cp["P1"], cp["P2"], cp["P3"], cp["P4"], cp["P5"]])
    kx, kz   = keel_pts[:, 0], keel_pts[:, 2]
    # Parametric B-spline interpolating through all 5 keel control points
    tck_keel, _ = splprep([kx, kz], s=0, k=min(3, len(kx)-1))
    u_fine      = np.linspace(0, 1, 300)
    kx_fit, kz_fit = splev(u_fine, tck_keel)
    results["keel_spline_pts"] = keel_pts
    results["keel_spline_curve"] = np.column_stack([kx_fit, kz_fit])
    results["tck_keel"] = tck_keel

    # ── 2. Deck edge spline (x-y plane, top view) ────────────────────────────
    # Control points: D1, D2, D3, D4
    deck_pts = np.array([cp["D1"], cp["D2"], cp["D3"], cp["D4"]])
    dx, dy   = deck_pts[:, 0], deck_pts[:, 1]
    tck_deck, _ = splprep([dx, dy], s=0, k=min(3, len(dx)-1))
    dx_fit, dy_fit = splev(u_fine, tck_deck)
    results["deck_spline_pts"] = deck_pts
    results["deck_spline_curve"] = np.column_stack([dx_fit, dy_fit])
    results["tck_deck"] = tck_deck

    # ── 3. Keel planform spline (x-y plane, bottom view) ─────────────────────
    # Control points: B1, B2, B3, B4
    bot_pts  = np.array([cp["B1"], cp["B2"], cp["B3"], cp["B4"]])
    bx, by   = bot_pts[:, 0], bot_pts[:, 1]
    tck_bot, _ = splprep([bx, by], s=0, k=min(3, len(bx)-1))
    bx_fit, by_fit = splev(u_fine, tck_bot)
    results["bot_spline_pts"]   = bot_pts
    results["bot_spline_curve"] = np.column_stack([bx_fit, by_fit])
    results["tck_bot"] = tck_bot

    # ── 4. Hull offset table: half-beam B(x) and draft T(x) ──────────────────
    # Sample the actual hull geometry at many stations, then fit cubic splines.
    # This is the most useful output for a naval architect — it's the offset
    # table that completely defines the hull form.
    xs_stations = np.linspace(0.02 * L, 0.98 * L, 60)
    y_hw = _sort_interp(cv["deck_edge"], xs_stations, col=1)   # half-beam
    z_k  = _sort_interp(cv["keel_line"], xs_stations, col=2)   # keel z
    draft_stations = np.maximum(-z_k, 0.0)

    # Cubic spline fits — smooth interpolation of the offset table
    cs_beam  = CubicSpline(xs_stations, y_hw,           bc_type="natural")
    cs_draft = CubicSpline(xs_stations, draft_stations,  bc_type="natural")

    xs_fine  = np.linspace(0.02 * L, 0.98 * L, 500)
    results["offset_xs"]       = xs_stations
    results["offset_beam"]     = y_hw
    results["offset_draft"]    = draft_stations
    results["cs_beam"]         = cs_beam
    results["cs_draft"]        = cs_draft
    results["beam_fit_curve"]  = np.column_stack([xs_fine, cs_beam(xs_fine)])
    results["draft_fit_curve"] = np.column_stack([xs_fine, cs_draft(xs_fine)])

    return results


def export_offset_table(
    spline_results: dict,
    path: str = "hull_offset_table.csv",
    n_export: int = 50,
) -> None:
    """
    Export the smooth hull offset table to CSV.

    Columns: x [m], half_beam [m], draft [m], section_area [m²]

    This is the primary geometry deliverable — it fully defines the
    hull form as a function of longitudinal position.
    """
    L       = CONST["L"]
    xs      = np.linspace(0.02 * L, 0.98 * L, n_export)
    beam    = spline_results["cs_beam"](xs)
    draft   = spline_results["cs_draft"](xs)
    sec_area = 0.5 * np.pi * beam * draft  # elliptic section area

    import csv
    with open(path, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["x_m", "half_beam_m", "draft_m", "section_area_m2"])
        for i in range(n_export):
            w.writerow([f"{xs[i]:.4f}", f"{beam[i]:.5f}",
                        f"{draft[i]:.5f}", f"{sec_area[i]:.5f}"])
    print(f"  Exported offset table → {path}  ({n_export} stations)")


