def cross_section_bezier(cv: dict) -> tuple:
    curve = cv["front_xsec"]
    return curve[:, 1], curve[:, 2]


def cross_section_ellipse(x_station: float, cv: dict, n: int = 200) -> tuple:
    half_beam = float(_sort_interp(cv["deck_edge"], np.array([x_station]), col=1))
    z_keel    = float(_sort_interp(cv["keel_line"], np.array([x_station]), col=2))
    draft     = max(-z_keel, 0.0)
    y = np.linspace(-half_beam, half_beam, n)
    z = (-draft * np.sqrt(np.maximum(1.0 - (y / half_beam)**2, 0.0))
         if half_beam > 1e-9 and draft > 1e-9
         else np.zeros_like(y))
    return y, z


