# ============================================================
# 8)  PAPER-STYLE 3-VIEW HULL GEOMETRY PLOT
# ============================================================
#
# Reproduces the figure layout from the research paper:
#
#   (a) PROFILE VIEW  (x-z plane, side view)
#       Shows: keel Bezier P1→P2→P3→P4→P5→P6, deck line,
#              chine curve C1→C2→C3→C4→C5, all control points labelled
#
#   (b) DECK VIEW  (x-y plane, top-down)
#       Shows: deck edge D1→D2→D3→D4, chine C1→C2→C3→C4→C5,
#              keel planform B1→B2→B3→B4, all control points labelled
#
#   (c) FRONT VIEW  (y-z plane, bow-on cross-section)
#       Shows: bow cross-section Bezier with FK (keel depth) and FW (hull width)
#
# Control point naming matches the paper diagram as closely as possible
# given our parameterisation (P1..P5, C1..C5, D1..D4, B1..B4, FK, FW).
#

def plot_paper_style_views(
    h_opt:  np.ndarray,
    h_init: np.ndarray,
    R_init: float,
    V_init: float,
    R_opt:  float,
    V_opt:  float,
) -> None:
    """
    Paper-style three-view drawing of the optimal hull with labelled
    control points, matching the layout from the research paper image.

    Shows initial hull (blue dashed) and optimal hull (orange solid)
    overlaid on all three views.
    """
    cp_i = build_control_points(h_init, **CONST)
    cp_o = build_control_points(h_opt,  **CONST)
    cv_i = hull_curves(cp_i, n=400)
    cv_o = hull_curves(cp_o, n=400)

    L, B, T, F = CONST["L"], CONST["B"], CONST["T"], CONST["F"]

    BLUE   = "steelblue"
    ORANGE = "darkorange"
    RED    = "#c0392b"
    GREEN  = "#27ae60"
    GOLD   = "#d4ac0d"
    PURPLE = "#8e44ad"

    fig = plt.figure(figsize=(18, 14))
    fig.patch.set_facecolor("white")
    fig.suptitle(
        f"Optimal Hull Geometry — Three-View Drawing\n"
        f"Initial:  R = {R_init:.3f} N,  V = {V_init:.4f} m³     "
        f"Optimal:  R = {R_opt:.3f} N  ({100*(R_init-R_opt)/R_init:.1f}% reduction),  "
        f"V = {V_opt:.4f} m³",
        fontsize=13, fontweight="bold", y=0.98,
    )

    gs = gridspec.GridSpec(
        2, 2,
        figure=fig,
        height_ratios=[1.1, 1.0],
        hspace=0.38, wspace=0.28,
    )

    # =========================================================================
    # (a) PROFILE VIEW  —  x-z plane  (top row, full width)
    # =========================================================================
    ax_prof = fig.add_subplot(gs[0, :])

    for cv, cp, color, lw, ls, alpha, tag in [
        (cv_i, cp_i, BLUE,   1.6, "--", 0.65, "Initial"),
        (cv_o, cp_o, ORANGE, 2.5, "-",  1.00, "Optimal"),
    ]:
        keel = cv["keel_line"]
        deck = cv["deck_line"]

        # Deck (flat line at z=F)
        ax_prof.plot(deck[:, 0], deck[:, 2],
                     color=color, lw=lw, ls=ls, alpha=alpha,
                     label=f"{tag}  R={compute_R_and_V(h_opt if tag=='Optimal' else h_init, **CONST)[0]:.3f} N")

        # Keel Bezier
        ax_prof.plot(keel[:, 0], keel[:, 2],
                     color=color, lw=lw, ls=ls, alpha=alpha)

        # Bow vertical from deck to P2
        ax_prof.plot([0, 0], [F, cp["P2"][2]],
                     color=color, lw=max(lw-0.6, 0.8), ls=":", alpha=alpha)

        # Fill hull cross-section
        xq  = np.linspace(0, L, 400)
        idx = np.argsort(keel[:, 0])
        zk  = np.interp(xq, keel[idx, 0], keel[idx, 2])
        ax_prof.fill_between(xq, np.full_like(xq, F), zk,
                             alpha=0.10 if tag == "Initial" else 0.18,
                             color=color)

    # Label optimal control points only (paper style: dots + labels)
    keel_pts_opt = np.array([
        cp_o["P1"], cp_o["P2"], cp_o["P3"], cp_o["P4"], cp_o["P5"]
    ])
    pt_names = ["P₁(0,0,F)", "P₂(0,0,h₁)", "P₃(h₂,0,T)", "P₄(h₃,0,T)", "P₅(L,0,F)"]
    pt_colors = [RED, RED, RED, RED, RED]
    offsets   = [(6, 5), (6, -14), (4, 6), (4, 6), (-50, 5)]

    for pt, name, col, off in zip(keel_pts_opt, pt_names, pt_colors, offsets):
        ax_prof.scatter(pt[0], pt[2], s=60, color=col, zorder=8,
                        edgecolors="white", linewidths=0.7)
        ax_prof.annotate(name, (pt[0], pt[2]),
                         textcoords="offset points", xytext=off,
                         fontsize=8, color=col, fontweight="bold",
                         bbox=dict(boxstyle="round,pad=0.15", fc="white",
                                   ec="none", alpha=0.7))

    # Waterline
    ax_prof.axhline(0, color="navy", lw=1.0, ls=":", alpha=0.7, label="Waterline (z=0)")

    # Annotations: dimension arrows
    ax_prof.annotate("", xy=(L, -T - 0.03), xytext=(0, -T - 0.03),
                     arrowprops=dict(arrowstyle="<->", color="gray", lw=1.0))
    ax_prof.text(L/2, -T - 0.055, f"L = {L:.2f} m", ha="center", va="top",
                 fontsize=8, color="gray")
    ax_prof.annotate("", xy=(-0.06, F), xytext=(-0.06, -T),
                     arrowprops=dict(arrowstyle="<->", color="gray", lw=1.0))
    ax_prof.text(-0.085, (F - T)/2, f"F+T\n={F+T:.2f}m", ha="center", va="center",
                 fontsize=7, color="gray")

    ax_prof.set_xlabel("x  [m]  — Length", fontsize=11)
    ax_prof.set_ylabel("z  [m]  — Height", fontsize=11)
    ax_prof.set_title("(a) Profile View  —  x-z plane\n"
                      "[Bezier keel:  P₁→P₂→P₃→P₄→P₅,  flat deck at z = F]",
                      fontsize=10)
    ax_prof.legend(fontsize=8, loc="upper right")
    ax_prof.set_xlim(-0.10, L + 0.05)
    ax_prof.set_ylim(-T - 0.09, F + 0.08)
    ax_prof.set_aspect("equal")
    ax_prof.grid(True, alpha=0.20)

    # =========================================================================
    # (b) DECK VIEW  —  x-y plane  (bottom-left)
    # =========================================================================
    ax_deck = fig.add_subplot(gs[1, 0])

    for cv, cp, color, lw, ls, alpha, tag in [
        (cv_i, cp_i, BLUE,   1.6, "--", 0.65, "Initial"),
        (cv_o, cp_o, ORANGE, 2.5, "-",  1.00, "Optimal"),
    ]:
        de = cv["deck_edge"]
        kp = cv["keel_plan"]

        # Deck edge (starboard) and its mirror (port)
        ax_deck.plot(de[:, 0],  de[:, 1], color=color, lw=lw, ls=ls, alpha=alpha,
                     label=f"{tag}" if tag == "Initial" else f"{tag} (optimal)")
        ax_deck.plot(de[:, 0], -de[:, 1], color=color, lw=lw, ls=ls, alpha=alpha)

        # Keel planform (starboard + port)
        ax_deck.plot(kp[:, 0],  kp[:, 1], color=color, lw=max(lw-0.5, 1.0),
                     ls=ls, alpha=alpha * 0.8)
        ax_deck.plot(kp[:, 0], -kp[:, 1], color=color, lw=max(lw-0.5, 1.0),
                     ls=ls, alpha=alpha * 0.8)

        # Fill hull footprint
        ax_deck.fill_between(de[:, 0],  de[:, 1], -de[:, 1],
                             alpha=0.08 if tag == "Initial" else 0.15,
                             color=color)

    # Label deck edge control points on optimal hull
    deck_pts = [cp_o["D1"], cp_o["D2"], cp_o["D3"], cp_o["D4"]]
    d_names  = ["D₁(0,B,F)", "D₂(h₆,h₇,F)", "D₃(h₈,h₉,F)", "D₄(h₁₀,h₁₁,F)"]
    d_off    = [(-5, 8), (4, 8), (4, 8), (4, -14)]
    for pt, name, off in zip(deck_pts, d_names, d_off):
        ax_deck.scatter(pt[0],  pt[1], s=55, color=GREEN, zorder=8,
                        edgecolors="white", linewidths=0.7)
        ax_deck.scatter(pt[0], -pt[1], s=55, color=GREEN, zorder=8,
                        edgecolors="white", linewidths=0.7)
        ax_deck.annotate(name, (pt[0], pt[1]),
                         textcoords="offset points", xytext=off,
                         fontsize=7, color=GREEN, fontweight="bold",
                         bbox=dict(boxstyle="round,pad=0.1", fc="white",
                                   ec="none", alpha=0.7))

    # Label keel planform control points
    bot_pts = [cp_o["B1"], cp_o["B2"], cp_o["B3"], cp_o["B4"]]
    b_names = ["B₁(h₂₄,0,T)", "B₂(h₂₅,h₂₆,T)", "B₃(h₂₇,h₂₈,T)", "B₄(h₂₉,0,T)"]
    b_off   = [(2, -14), (4, -14), (4, -14), (-55, -14)]
    for pt, name, off in zip(bot_pts, b_names, b_off):
        ax_deck.scatter(pt[0],  pt[1], s=55, color=GOLD, zorder=8,
                        edgecolors="white", linewidths=0.7)
        ax_deck.scatter(pt[0], -pt[1], s=55, color=GOLD, zorder=8,
                        edgecolors="white", linewidths=0.7)
        ax_deck.annotate(name, (pt[0], pt[1]),
                         textcoords="offset points", xytext=off,
                         fontsize=7, color=GOLD, fontweight="bold",
                         bbox=dict(boxstyle="round,pad=0.1", fc="white",
                                   ec="none", alpha=0.7))

    ax_deck.axhline(0, color="gray", lw=0.7, ls="--", alpha=0.5, label="Centreline")
    ax_deck.set_xlabel("x  [m]  — Length", fontsize=10)
    ax_deck.set_ylabel("y  [m]  — Half-beam", fontsize=10)
    ax_deck.set_title("(b) Deck / Plan View  —  x-y plane\n"
                      "[Green = deck edge D₁..D₄,  Gold = keel plan B₁..B₄]",
                      fontsize=10)
    ax_deck.legend(fontsize=7, loc="upper right")
    ax_deck.set_xlim(-0.03, L + 0.03)
    ax_deck.set_aspect("equal")
    ax_deck.grid(True, alpha=0.20)

    # =========================================================================
    # (c) FRONT VIEW  —  y-z plane  (bottom-right)
    # =========================================================================
    ax_front = fig.add_subplot(gs[1, 1])

    stations = [
        (0.05 * L, "-",  1.0, "Bow  (x=0.05L)"),
        (0.50 * L, "--", 0.8, "Mid  (x=0.50L)"),
        (0.90 * L, ":",  0.7, "Aft  (x=0.90L)"),
    ]

    for cv, cp, color, base_lw, base_alpha in [
        (cv_i, cp_i, BLUE,   1.4, 0.60),
        (cv_o, cp_o, ORANGE, 2.2, 1.00),
    ]:
        for x_st, ls, alpha_fac, st_label in stations:
            y_cs, z_cs = cross_section_ellipse(x_st, cv)
            hb = float(_sort_interp(cv["deck_edge"], np.array([x_st]), col=1))
            lbl = (f"{'Initial' if color==BLUE else 'Optimal'}  {st_label}"
                   if color == ORANGE else None)
            ax_front.plot(y_cs, z_cs, color=color, lw=base_lw,
                          ls=ls, alpha=base_alpha * alpha_fac, label=lbl)
            ax_front.plot([-hb, hb], [0, 0],
                          color=color, lw=0.7, ls=ls,
                          alpha=base_alpha * alpha_fac * 0.5)
            if color == ORANGE:
                ax_front.fill_between(y_cs, z_cs, 0, alpha=0.06, color=color)

        # Bow Bezier cross-section (most detailed)
        y_bow, z_bow = cross_section_bezier(cv)
        ax_front.plot(y_bow, z_bow, color=color, lw=base_lw + 0.5,
                      ls="-", alpha=base_alpha, zorder=5)

        # FK and FW control points (optimal only)
        if color == ORANGE:
            FK, FW = cp["FK"], cp["FW"]
            ax_front.scatter(FK[1], FK[2], s=100, color=PURPLE,
                             zorder=9, edgecolors="white", linewidths=0.8,
                             marker="^", label="FK  keel depth")
            ax_front.annotate(
                f"FK(0,0,−h₁₆·T)\n= (0,0,{FK[2]:.3f})",
                (FK[1], FK[2]),
                textcoords="offset points", xytext=(8, -18),
                fontsize=7, color=PURPLE, fontweight="bold",
                bbox=dict(boxstyle="round,pad=0.15", fc="white",
                          ec="none", alpha=0.8),
            )
            for sign, side in [(+1, "stbd"), (-1, "port")]:
                ax_front.scatter(sign * FW[1], FW[2], s=100, color=PURPLE,
                                 zorder=9, edgecolors="white", linewidths=0.8,
                                 marker="s")
            ax_front.annotate(
                f"FW(0,h₁₇·B,0)\n= (0,±{FW[1]:.3f},0)",
                (FW[1], FW[2]),
                textcoords="offset points", xytext=(6, 6),
                fontsize=7, color=PURPLE, fontweight="bold",
                bbox=dict(boxstyle="round,pad=0.15", fc="white",
                          ec="none", alpha=0.8),
            )

    ax_front.axhline(0, color="navy", lw=1.0, ls=":", alpha=0.7, label="Waterline")
    ax_front.axvline(0, color="gray",  lw=0.6, ls="--", alpha=0.4, label="Centreline")

    ax_front.set_xlabel("y  [m]  — Half-beam", fontsize=10)
    ax_front.set_ylabel("z  [m]  — Height",    fontsize=10)
    ax_front.set_title("(c) Front / Bow-On View  —  y-z plane\n"
                       "[solid=bow Bezier,  dashed=mid station,  dotted=aft station]",
                       fontsize=10)
    ax_front.set_aspect("equal")
    ax_front.grid(True, alpha=0.20)
    ax_front.legend(handles=[
        Line2D([], [], color=BLUE,   lw=2, ls="--", label="Initial hull"),
        Line2D([], [], color=ORANGE, lw=2.5,        label="Optimal hull"),
        Line2D([], [], color="k",    lw=2.0, ls="-",  label="Bow station"),
        Line2D([], [], color="k",    lw=1.5, ls="--", label="Mid station"),
        Line2D([], [], color="k",    lw=1.5, ls=":",  label="Aft station"),
        Line2D([], [], color=PURPLE, lw=0, marker="^", markersize=7,
               label="FK  keel depth"),
        Line2D([], [], color=PURPLE, lw=0, marker="s", markersize=7,
               label="FW  hull width"),
    ], fontsize=7, loc="lower center", ncol=2)

    plt.savefig("hull_three_view.png", dpi=160, bbox_inches="tight",
                facecolor="white")
    print("  Saved → hull_three_view.png")
    plt.show()


