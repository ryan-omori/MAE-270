# ============================================================
# 3)  CSDL OPTIMIZATION PROBLEM
# ============================================================

def build_problem(
    R_surr: dict,
    V_surr: dict,
    V_ref: float,
    gamma: float = 0.02,
    h0: np.ndarray = None,
) -> tuple:
    """
    Build the CSDL optimization problem.

    Objective  : minimise R_surr(h)
    Constraint : (1-γ)·V_ref ≤ V_surr(h) ≤ (1+γ)·V_ref
    Design var : h ∈ [0, 1]^29

    The volume band constraint preserves displacement —
    so we reduce resistance without shrinking the hull.
    """
    if h0 is None:
        h0 = 0.5 * np.ones(29)

    rec = csdl.Recorder(inline=True)
    rec.start()

    h = csdl.Variable(value=h0.copy(), name="h")
    h.set_as_design_variable(lower=np.zeros(29), upper=np.ones(29))

    # Direct surrogates — single-stage, no CP intermediate
    R_hat = predict_rbf_csdl(h, R_surr)
    V_hat = predict_rbf_csdl(h, V_surr)

    R_hat.set_as_objective()
    ((1.0 - gamma) * V_ref - V_hat).set_as_constraint(upper=0.0)  # lower bound
    (V_hat - (1.0 + gamma) * V_ref).set_as_constraint(upper=0.0)  # upper bound

    rec.stop()
    sim  = csdl.experimental.PySimulator(rec)
    prob = opt.CSDLAlphaProblem(problem_name="HullOpt", simulator=sim)
    prob.x0 = h0.copy()
    return prob, sim


