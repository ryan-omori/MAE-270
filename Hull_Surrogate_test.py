"""
hull_mdo_surrogate.py
=====================
Surrogate-based hull MDO pipeline.

Workflow
--------
1. Load the full dataset from hull_dataset.csv  (generated by generate_dataset.py)
2. Split 50/50 into train and test sets
3. Fit RBF surrogates on the TRAIN half:   h → R_surr,  h → V_surr
4. Validate on the TEST half:
     compare surrogate predictions vs truth-model values
     → this confirms the surrogate generalises before we trust the optimizer
5. Optimize with CSDL + SQP:
     minimise R_surr(h)  subject to  |V_surr(h) - V_ref| ≤ γ·V_ref
6. Evaluate the optimal h on the TRUTH model and report results
7. Plot initial vs optimal hull geometry

Why RBF instead of a polynomial?
---------------------------------
The original quadratic polynomial surrogate used features [1, z, z²] with
no cross-terms. Resistance is nonlinear and depends on interactions between
parameters (e.g. bow sharpness × fullness). A radial basis function (RBF)
with a thin-plate-spline kernel  φ(r) = r² log(r)  captures these
interactions naturally and is smooth enough for gradient-based optimization.

Usage
-----
    # First generate data (only needs to be done once):
    python generate_dataset.py

    # Then run the surrogate pipeline:
    python hull_mdo_surrogate.py
    python hull_mdo_surrogate.py --csv hull_dataset.csv --gamma 0.03
"""

